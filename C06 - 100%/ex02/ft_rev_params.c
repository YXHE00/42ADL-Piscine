#include<unistd.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac > 1)
	{
		ac--;
		while (ac >= 1)
		{
			i = 0;
			while (av[ac][i])
			{
				write(1, &av[ac][i], 1);
				i++;
			}
			write(1, "\n", 1);
			ac--;
		}
	}
	return (0);
}
